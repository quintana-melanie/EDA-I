# Uso de listas en Python

numeros = [5, 10, 15, 20]

print("Lista completa:", numeros)
print("Primer elemento:", numeros[0])

numeros.append(25)
print("Lista después de agregar:", numeros)

numeros.remove(10)
print("Después de eliminar 10:", numeros)

print("Longitud:", len(numeros))

